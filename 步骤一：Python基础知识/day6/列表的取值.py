#定义一个列表
list = ["张三","李四","王五","赵六","钱七","孙八"]
#取值的语法：变量名 = 列表变量[索引值]
#将赵六从列表中取出来
zaoliu = list[3]
print(zaoliu)

#范围取值
#将李四到钱七之间全取出来
#范围取值的语法：变量名 = 列表变量[起始索引：结束索引]
#重点：在python中范围取值是左闭右开，包含左边的值，不包含右边值
fanwei = list[1:5]
print(fanwei)

#获取列表的索引值
#使用index函数获取索引
#语法：变量 = 列表变量.index[元素名]
#获取孙八的索引值
sunba = list.index("孙八")
print(sunba)
